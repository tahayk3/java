public class Operadores {
    public static void main(String[] args) {
        /*
        OPERADORES ARITMETICOS
            + suma
            - resta
            * mutiplicacion
            / division
            % modulo
        */
        int num1 = 5;
        int num2 = 5;
        System.out.println(num1 + num2);

        /*
         OPERADORES RELACIONALES
            < menor que
            > mayor que
            <= menor o igual
            >= mayor o igual
            == igual
            != diferente
        */
        System.out.println(num1<num2);

        /*
         OPERADORES LOGICOS
            && AND
            || OR
            ! negacion
        */
        System.out.println(!true);


    }
}
