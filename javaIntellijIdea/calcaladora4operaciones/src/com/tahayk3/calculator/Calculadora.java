package com.tahayk3.calculator;

public class Calculadora {

    public float operacionSuma(float num1, float num2){
        return num1 + num2;
    }

    public float operacionResta(float num1, float num2){
        return num1 - num2;
    }

    public float operacionMul(float num1, float num2){;
        return num1 * num2;
    }

    public float operacionDiv(float num1, float num2){
        return num1 / num2;
    }
}
